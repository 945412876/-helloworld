package variable;

public class variable {
    public static void main(String[] args) {
            // 目标学会使用变量
        // 数据类型 变量名称 =初始值
        double money =114514.19;
        System.out.println(money);
        // 收红包 （从右往左看）
        money  = money + 4.0;
        System.out.println(money);
        //从上往下执行，先扫到变量 ，然后改变，再扫到变量
        int age =18;
        System.out.println(age);
        age =25;
        System.out.println(age);

    }
}
